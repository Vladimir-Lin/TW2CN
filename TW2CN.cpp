class Strings
{
  public:

    QMap<QString,QString> TraditionalToSimplified ;
    QMap<QString,QString> SimplifiedToTraditional ;

    explicit Strings          (void) ;
    virtual ~Strings          (void) ;

    void     setupTranslation (void) ;
    QString  ToTraditional    (QString name) ;
    QString  ToSimplified     (QString name) ;

};

Strings:: Strings (void)
{
}

Strings::~Strings (void)
{
}

void Strings::setupTranslation(void)
{
  QByteArray T ( (const char *)TraditionalSource ) ;
  QByteArray S ( (const char *)SimplifiedSource  ) ;
  QString    t = QString::fromUtf8(T)              ;
  QString    s = QString::fromUtf8(S)              ;
  TraditionalToSimplified.clear()                  ;
  SimplifiedToTraditional.clear()                  ;
  for (int i=0;i<t.length();i++)                   {
    QString A = t.at(i)                            ;
    QString B = s.at(i)                            ;
    TraditionalToSimplified [A] = B                ;
    SimplifiedToTraditional [B] = A                ;
  }                                                ;
}

QString Strings::ToTraditional(QString name)
{
  QString z ;
  for (int i=0;i<name.length();i++) {
    QString n = name.at(i) ;
    if (SimplifiedToTraditional.contains(n))
         z.append(SimplifiedToTraditional[n]);
    else z.append(n) ;
  };
  return  z ;
}

QString Strings::ToSimplified(QString name)
{
  QString z ;
  for (int i=0;i<name.length();i++) {
    QString n = name.at(i) ;
    if (TraditionalToSimplified.contains(n))
         z.append(TraditionalToSimplified[n]);
    else z.append(n) ;
  };
  return  z ;
}

QFileInfoList FileInfos(QDir & dir,bool tracing)
{
  QFileInfoList fi = dir.entryInfoList(QDir::AllEntries|QDir::NoDotAndDotDot) ;
  QFileInfoList target                                                        ;
  for (int i=0;i<fi.size();)                                                  {
    if ("." ==fi[i].fileName()) fi.removeAt(i) ;                           else
    if (".."==fi[i].fileName()) fi.removeAt(i) ;                           else
    i++                                                                       ;
  }                                                                           ;
  target = fi                                                                 ;
  if ( ! tracing ) return target                                              ;
  for (int i=0;i<fi.size();i++) if (fi[i].isDir())                            {
    QDir d(fi[i].absoluteFilePath())                                          ;
    QFileInfoList ni                                                          ;
    ni = FileInfos ( d , true )                                               ;
    target += ni                                                              ;
  }                                                                           ;
  return target                                                               ;
}

bool toByteArray(QString filename,QByteArray & Body)
{
  QFile F ( filename )                             ;
  if ( !F.open(QIODevice::ReadOnly) ) return false ;
  Body = F . readAll ( )                           ;
  F        . close   ( )                           ;
  return ( Body.size() > 0 )                       ;
}

bool toFile(QString filename,QByteArray & Body)
{
  QFile F ( filename )                              ;
  if ( !F.open(QIODevice::WriteOnly) ) return false ;
  qint64 size = F . write ( Body )                  ;
  F . close   ( )                                   ;
  return ( Body.size() == size )                    ;
}

QStringList StringList(QString filename,QString split)
{
  QStringList s                                ;
  QString     b                                ;
  QByteArray  Body                             ;
  if ( ! toByteArray(filename,Body) ) return s ;
  b = QString::fromUtf8(Body)                  ;
  s = b.split(split)                           ;
  return s                                     ;
}

QStringList PurifyLines(QStringList lines)
{
  QStringList s                     ;
  for (int i=0;i<lines.count();i++) {
    QString l = lines[i]            ;
    l = l.replace("\n","")          ;
    l = l.replace("\r","")          ;
    s << l                          ;
  }                                 ;
  return s                          ;
}

int main( int argc , char * argv [ ] )
{
  QDir          dd                                                ;
  Strings       ss                                                ;
  QFileInfoList ff                                                ;
  QByteArray    bb                                                ;
  QString       tt                                                ;
  QString       tw                                                ;
  QString       cn                                                ;
  QString       rp                                                ;
  QString       suffix = "html"                                   ;
  QStringList   TW                                                ;
  QStringList   CN                                                ;
  QStringList   MP                                                ;
  bool          tracing = false                                   ;
  bool          look    = true                                    ;
  /////////////////////////////////////////////////////////////////
  if ( argc > 1 )                                                 {
    int ix = 1                                                    ;
    while ( ix < argc )                                           {
      if ( 0 == strcmp(argv[ix],"--suffix" ) )                    {
        ix++                                                      ;
        suffix = argv[ix]                                         ;
        ix++                                                      ;
      } else
      if ( 0 == strcmp(argv[ix],"--all"  ) )                      {
        tracing = true                                            ;
        ix++                                                      ;
      } else
      if ( 0 == strcmp(argv[ix],"--files") )                      {
        ix++                                                      ;
        while ( ix < argc )                                       {
          ff << QFileInfo ( argv [ ix ] )                         ;
          ix++                                                    ;
        }                                                         ;
        look = false                                              ;
      } else ix++                                                 ;
    }                                                             ;
  }                                                               ;
  /////////////////////////////////////////////////////////////////
  tw = "<meta http-equiv=\"Content-Language\" content=\"zh-TW\">" ;
  cn = "<meta http-equiv=\"Content-Language\" content=\"zh-CN\">" ;
  /////////////////////////////////////////////////////////////////
  ss . setupTranslation ( )                                       ;
  /////////////////////////////////////////////////////////////////
  rp = dd.absoluteFilePath("Replacements.txt")                    ;
  MP = StringList  ( rp , "\n" )                                  ;
  MP = PurifyLines ( MP        )                                  ;
  /////////////////////////////////////////////////////////////////
  foreach ( tt , MP )                                             {
    QStringList L = tt.split(" ")                                 ;
    if ( L.count() == 2 )                                         {
      TW << L [ 0 ]                                               ;
      CN << L [ 1 ]                                               ;
    }                                                             ;
  }                                                               ;
  /////////////////////////////////////////////////////////////////
  if ( look ) ff = FileInfos ( dd , tracing )                     ;
  for (int i=0;i<ff.count();i++)                                  {
    if (ff[i].suffix()==suffix)                                   {
      QString fn = ff[i].absoluteFilePath()                       ;
      bb.clear()                                                  ;
      toByteArray ( fn , bb )                                     ;
      if (bb.size()>0)                                            {
        tt = QString::fromUtf8 ( bb      )                        ;
        for (int i=0;i<TW.count();i++)                            {
          tt = tt . replace ( TW[i] , CN[i] )                     ;
        }                                                         ;
        tt = ss . ToSimplified ( tt      )                        ;
        tt = tt . replace      ( tw , cn )                        ;
        for (int i=0;i<TW.count();i++)                            {
          tt = tt . replace ( TW[i] , CN[i] )                     ;
        }                                                         ;
        bb = tt . toUtf8       (         )                        ;
        if ( bb . size ( ) > 0 ) toFile ( fn , bb )               ;
      }                                                           ;
    }                                                             ;
  }                                                               ;
  return 0                                                        ;
}
